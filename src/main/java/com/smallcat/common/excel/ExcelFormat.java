package com.smallcat.common.excel;

public final class ExcelFormat {
	
	/** 列对应标题名称 **/
	private String colTitleName;
	/** 列对应属性名称 **/
	private String colMethodName;
	/** 列对应数据类型 **/
	private String colDataType;
	
	public String getColTitleName() {
		return colTitleName;
	}
	public void setColTitleName(String colTitleName) {
		this.colTitleName = colTitleName;
	}
	public String getColMethodName() {
		return colMethodName;
	}
	public void setColMethodName(String colMethodName) {
		this.colMethodName = colMethodName;
	}
	public String getColDataType() {
		return colDataType;
	}
	public void setColDataType(String colDataType) {
		this.colDataType = colDataType;
	}
	
	
	
	
}
